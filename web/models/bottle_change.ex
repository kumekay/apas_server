defmodule ApasServer.BottleChange do
  use ApasServer.Web, :model

  schema "bottle_changes" do

    timestamps()
  end

  @doc """
  Builds a changeset based on the `struct` and `params`.
  """
  def changeset(struct, params \\ %{}) do
    struct
    |> cast(params, [])
    |> validate_required([])
  end
end
